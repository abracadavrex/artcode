package week1;

public class DataTypeOperations {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int c = a+b;
        System.out.println(c);

        a = a + b;
        a += b;

//        / and %
        int d = 100;
        int e = 9;
        System.out.println(d%e);
    }
}
